#string replication 
a = "Hello "
b = "World "
c = "Python"

print(a * 3)   # Hello Hello Hello 
print(b * 2)   # World World 
print(c)       # Python

#string replication * and string concatenation +
a = "Hi"
b = "Bye"
c = "Hello"
d = "Python"

print(a * 2 + b * 3)             # HiHi-ByeByeBye
print(c * 2 + "-")               # HelloHello-
print(d * 4)                     # PythonPythonPythonPython