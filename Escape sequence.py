print('It\'s a beautiful day!')     # \' allows single quote inside single quotes
print("It's a beautiful day!")      # You can even use like this using ' under " is consider as normal text 
print("He said, \"Hello!\"")        # \" allows double quotes inside double quotes
print('He said,"Hello!"')           # You can even use like this using " under ' is consider as normal text 
print("Line 1\nLine 2")             # \n adds a new line
print("Tab\tSpace")                 # \t adds a tab space
print("Backslash: \\")              # \\ prints a backslash 
